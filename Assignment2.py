# Create a list named "Fruit" and assign 3 fruits to it.
fruits = [
    "Apple","Banana","Mango"
]
# 1. Print the value of fruits variable
print(fruits)
# 2. Add 2 more fruits at the least of the fruits list
fruits.append("Watermelon")
fruits.append("Grapes")
# 3. Print the value of fruits variable
print(fruits)
# 4. Remove 1 fruit from the list
fruits.remove("Watermelon")
# 5. Print the value of fruits variable
print(fruits)
# Slice first 3 value and print
print(fruits[0:2])
# Slice last 3 value and print
print(fruits[-3:-1])
#reverse the list
print(fruits[::-1])
